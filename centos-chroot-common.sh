# Set the CHROOT environment variable to the location of the chroot to be created:
export CHROOT=/disk1/centos-chroot
# Set the MIRROR environment variable to the location of the release RPM:
export MIRROR=ftp://ftp.uninett.no/pub/linux/centos/7/os/x86_64/Packages/
# Set the RELEASE_RPM environment variable to the file name of the release RPM:
export RELEASE_RPM=centos-release-7-0.1406.el7.centos.2.3.x86_64.rpm
# Set the RELEASE_VERSION environment variable to the release required:
export RELEASE_VERSION=7
